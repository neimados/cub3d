#include "../includes/cub3d.h"

int	ft_check_color(char *tmp)
{
	int	i;

	i = 0;
	while (tmp[i])
	{
		if (tmp[i] == ',')
		{
			if (tmp[i + 1])
				if (tmp[i + 1] == ',')
					return (1);
		}
		i++;
	}
	if (tmp[i - 1] == ',' || tmp[0] == ',')
		return (1);
	return (0);
}
